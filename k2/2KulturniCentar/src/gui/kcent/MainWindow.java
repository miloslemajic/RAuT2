    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String manifestacija = tf1_man.getText();
        String organizator = tf2_man.getText();
        String tip = cb1_man.getSelectedItem().toString();
        String sala = "";
        
        Integer rb = sl1_man.getValue();
        
        if(rb1_man.isSelected())
        {
            sala= "Mala";
            prihodi += 105. * rb.doubleValue();
            rashodi += 1000.;
        }
        if(rb2_man.isSelected())
        {
            sala= "Srednja";
            prihodi += 55. * rb.doubleValue();
            rashodi += 2000.;
        }
        if(rb3_man.isSelected())
        {
            sala= "Velika";
            prihodi += 50. * rb.doubleValue();
            rashodi += 3000.;
        }
        
        model.addRow(new Object[] {manifestacija,organizator,tip,sala,rb});
        
        dialogManifestacija.dispose();
        
        tf_prihodi.setText(prihodi.toString());
        tf_rashodi.setText(rashodi.toString());
    }//GEN-LAST:event_jButton2ActionPerformed

