    private void bt_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editActionPerformed
        if(tb.getSelectedRow() > -1){
            String manifestacija = model.getValueAt(tb.getSelectedRow(), 0).toString();
            String organizator = model.getValueAt(tb.getSelectedRow(), 1).toString();
            String tip = model.getValueAt(tb.getSelectedRow(), 2).toString();
            String sala = model.getValueAt(tb.getSelectedRow(), 3).toString();

            if(sala== "Mala"){
                rb1_man.setSelected(true);
                ((SpinnerNumberModel)sp1_man.getModel()).setMaximum(20);
                ((SpinnerNumberModel)sp1_man.getModel()).setMinimum(0);
                sl1_man.setMaximum(20);
                sl1_man.setMinimum(0);
            }
            if(sala== "Srednja"){
                rb2_man.setSelected(true);
                ((SpinnerNumberModel)sp1_man.getModel()).setMaximum(50);
                ((SpinnerNumberModel)sp1_man.getModel()).setMinimum(0);
                sl1_man.setMaximum(50);
                sl1_man.setMinimum(0);
            }
            if(sala== "Velika"){
                rb3_man.setSelected(true);
                ((SpinnerNumberModel)sp1_man.getModel()).setMaximum(100);
                ((SpinnerNumberModel)sp1_man.getModel()).setMinimum(0);
                sl1_man.setMaximum(100);
                sl1_man.setMinimum(0);
            }

            Integer rb = (Integer) model.getValueAt(tb.getSelectedRow(), 4);
            
            tf1_man.setText(manifestacija);
            tf2_man.setText(organizator);
            
            cb1_man.setSelectedItem(tip);
            
            sp1_man.setValue(rb);
            sl1_man.setValue(rb);
            
            dialogManifestacija.setVisible(true);
        }
    }//GEN-LAST:event_bt_editActionPerformed

    private void bt_resActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_resActionPerformed
        if(tb.getSelectedRow() > -1){
            Integer num = (Integer) model.getValueAt(tb.getSelectedRow(), 4);
            ((SpinnerNumberModel)sp_res.getModel()).setMaximum(num);
            ((SpinnerNumberModel)sp_res.getModel()).setMinimum(0);
            sl_res.setValue(0);
            sl_res.setMaximum(num);
            sl_res.setMinimum(0);
            dialogRezervacija.setVisible(true);
        }
    }//GEN-LAST:event_bt_resActionPerformed

