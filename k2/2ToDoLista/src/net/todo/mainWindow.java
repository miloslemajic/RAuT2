    private void tb1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1MouseClicked
        if(evt.getClickCount() == 2)
            izmena();
    }//GEN-LAST:event_tb1MouseClicked

    void izmena()
    {
        izmena = true;
            inputD.setVisible(true);
            
            DefaultTableModel model = (DefaultTableModel) tb1.getModel();

            tx1.setText(model.getValueAt(tb1.getSelectedRow(),1).toString());
            
            String tmp = model.getValueAt(tb1.getSelectedRow(),2).toString();
            
            if(tmp.equals("Posao"))
                rb1.setSelected(true);
            else
                rb1.setSelected(false);
            if(tmp.equals("Kupovina"))
                rb2.setSelected(true);
            else
                rb2.setSelected(false);
            if(tmp.equals("Ostalo"))
                rb3.setSelected(true);
            else
                rb3.setSelected(false);

           sp1.setValue(Integer.parseInt(model.getValueAt(tb1.getSelectedRow(),3).toString()));
    }
    
    
