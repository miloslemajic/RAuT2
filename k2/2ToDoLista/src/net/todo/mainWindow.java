    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tb1.getModel();
        
        String tip = "";
        
        if(rb1.isSelected())
            tip = "Posao";
        if(rb2.isSelected())
            tip = "Kupovina";
        if(rb3.isSelected())
            tip = "Ostalo";
        
        String unos = tx1.getText();
        
        if(izmena)
        {
            model.setValueAt(unos,tb1.getSelectedRow(),1);
            model.setValueAt(tip,tb1.getSelectedRow(),2);
            model.setValueAt(sp1.getValue(),tb1.getSelectedRow(),3);
        }
        else
            model.addRow(new Object[]{++rBr,unos,tip,sp1.getValue(),false});
        
        inputD.dispose();
    }//GEN-LAST:event_jButton5ActionPerformed

